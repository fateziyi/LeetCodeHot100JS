// 假设你正在爬楼梯。需要 n 阶你才能到达楼顶。

// 每次你可以爬 1 或 2 个台阶。你有多少种不同的方法可以爬到楼顶呢？
/**
 * @param {number} n
 * @return {number}
 */
var climbStairs = function (n) {
  // 1. 初始化数组 f，用于存储到达第 i 阶的方法数（dp 数组）
  const f = []
  // 2. 边界条件：
  // 爬 1 阶只有 1 种方法（直接爬 1 阶）
  f[1] = 1
  // 爬 2 阶有 2 种方法（1+1 或 2）
  f[2] = 2
  // 3. 递推计算：从第 3 阶开始，直到第 n 阶
  for (let i = 3; i <= n; i++) {
    // 核心递推公式：到达第 i 阶的方法数 = 到达 i-1 阶的方法数 + 到达 i-2 阶的方法数
    // 解释：最后一步要么是从 i-1 阶爬 1 阶上来，要么是从 i-2 阶爬 2 阶上来
    f[i] = f[i - 1] + f[i - 2]
  }
  // 4. 返回到达第 n 阶的方法数
  return f[n]
};