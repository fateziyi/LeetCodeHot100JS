// 你是一个专业的小偷，计划偷窃沿街的房屋。每间房内都藏有一定的现金，影响你偷窃的唯一制约因素就是相邻的房屋装有相互连通的防盗系统，如果两间相邻的房屋在同一晚上被小偷闯入，系统会自动报警。

// 给定一个代表每个房屋存放金额的非负整数数组，计算你 不触动警报装置的情况下 ，一夜之内能够偷窃到的最高金额。
/**
 * @param {number[]} nums
 * @return {number}
 */
var rob = function (nums) {
  // f0 代表 dp[i-2]（前i-2个房屋的最大金额），初始为0（没有房屋时金额为0）
  // f1 代表 dp[i-1]（前i-1个房屋的最大金额），初始为0（没有房屋时金额为0）
  let f0 = 0, f1 = 0;

  // 遍历每个房屋的金额x（相当于依次计算dp[1], dp[2], ..., dp[n]）
  for (const x of nums) {
    // 核心：同步更新f0和f1
    // 1. 新的f0 = 原来的f1（即dp[i-1] 变成新的 dp[i-2]）
    // 2. 新的f1 = max(原来的f1（不偷当前x）, 原来的f0 + x（偷当前x）)
    [f0, f1] = [f1, Math.max(f1, f0 + x)];
  }

  // 遍历结束后，f1 就是 dp[n]（前n个房屋的最大金额）
  return f1;
};